// Premitive types
// Dynamic Variables (We can change it any time)

let myName = "Rohit Singh";   // String
let isMam = true;           // Boolean
let roll = 10;              // Numbers
let value = undefined;      // Undefined (default)
let go = null;              // Null

console.log(typeof myName,typeof isMam,typeof roll,typeof value,go);

// Constant Variables (It can't be changed further)

const myName2 = "Rohit Singh";   
const isMam2 = true;           
const roll2 = 10;              
const value2 = undefined;      
const go2 = null;              

console.log(myName2,isMam2,roll2,value2,go2);